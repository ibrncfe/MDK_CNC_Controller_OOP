/// Draw single character (pixel coordinates)
/// @param    x point coordinate, x-axis
/// @param    y point coordinate, y-axis
/// @param    ch ASCII character
/// @param    color 16-bit color, default=white
/// @return   width of character in display pixels
uint16_t ILI9225_drawChar(int16_t Xpos, int16_t Ypos, unsigned char ch, uint16_t color, uint16_t bg) 
{
	uint32_t i = 0, j = 0;
	uint16_t height, width;
	uint8_t  offset;
	uint8_t  *pchar;
	uint32_t line;
	uint16_t l = 0;	
	
	height = 20; //in pixel-byte
	width = 21;	 //in pixel-bit
	offset =  8 *((width + 7)/8) -  width ;
	
	ch = ch - ' ';
	l = ch*60;
	uint8_t c[60] = {0};
	
	for(i=0; i<60; i++)
		c[i] = FontAvenirNext20_Table[l+i];

		for(i = 0; i < height; i++)
		{
    pchar = ((uint8_t *)c + (width + 7)/8 * i);
    
    switch(((width + 7)/8))
    {
      
    case 1:
      line =  pchar[0];      
      break;
      
    case 2:
      line =  (pchar[0]<< 8) | pchar[1];      
      break;
      
    case 3:
    default:
      line =  (pchar[0]<< 16) | (pchar[1]<< 8) | pchar[2];      
      break;
    } 
    
    for (j = 0; j < width; j++)
    {
      if(line & (1 << (width- j + offset- 1))) 
      {
        ILI9225_lcdDrawPixel((Xpos + j), Ypos, color);
      }
      else
      {
        ILI9225_lcdDrawPixel((Xpos + j), Ypos, bg);
      } 
    }
    Ypos++;
  }
		

	return 1;
}

