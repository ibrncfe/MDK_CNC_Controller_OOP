  ILI9225_WriteCommand16(0x20,0x0000); // Set GRAM Address
  ILI9225_WriteCommand16(0x21,0x0000); // Set GRAM Address

	/* Power ON sequence */
  HAL_Delay(50);                         // Delay 50ms
  ILI9225_WriteCommand16(0x10,0x0800); // Set SAP,DSTB,STB----0A00
  ILI9225_WriteCommand16(0x11,0x1F3F); // Set APON,PON,AON,VCI1EN,VC----1038
  HAL_Delay(50);                         // Delay 50ms
