	reset();
	
	CS_RESET;
	DelayMicro(20);
	
	ILI9225_WriteCommand16(0x01,0x011C);
	ILI9225_WriteCommand16(0x02,0x0100);
	ILI9225_WriteCommand16(0x03,0x1030);
  ILI9225_WriteCommand16(0x08,0x0808); // set BP and FP
  ILI9225_WriteCommand16(0x0B,0x1100); //frame cycle
  ILI9225_WriteCommand16(0x0C,0x0000); // RGB interface setting R0Ch=0x0110 for RGB 18Bit and R0Ch=0111for RGB16Bit
  ILI9225_WriteCommand16(0x0F,0x1401); // Set frame rate----0801
