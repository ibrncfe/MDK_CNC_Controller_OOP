  ILI9225_WriteCommand16(0x30,0x0000);
  ILI9225_WriteCommand16(0x31,0x00DB);
  ILI9225_WriteCommand16(0x32,0x0000);
  ILI9225_WriteCommand16(0x33,0x0000);
