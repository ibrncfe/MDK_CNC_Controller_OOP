//------------------------------------------------
/* Write Command 8bit then Data 16 bit*/
uint8_t ILI9225_WriteCommand16(uint8_t add, uint16_t reg)
{
	RS_CONTROL_RESET;	

	CS_RESET;

	if (HAL_SPI_Transmit(&hspi5,&add,1,10) != HAL_OK)
		return 0;
	uint8_t byte[2];
	byte[0] = (reg >> 8) & 0xFF;
	byte[1] = reg & 0xFF;
	RS_DATA_SET;
	if (HAL_SPI_Transmit(&hspi5, byte, 2, 10)!= HAL_OK)
		return 0;
	
	CS_SET;	

	return 1;
}
//-------------------------------------------------
