/// Draw circle
/// @param    x0 center, point coordinate, x-axis
/// @param    y0 center, point coordinate, y-axis
/// @param    radius radius
/// @param    color 16-bit color
void ILI9225_drawCircle(uint16_t x0, uint16_t y0, uint16_t r, uint16_t color) {

    int16_t f = 1 - r;
    int16_t ddF_x = 1;
    int16_t ddF_y = -2 * r;
    int16_t x = 0;
    int16_t y = r;

    ILI9225_lcdDrawPixel(x0, y0 + r, color);
    ILI9225_lcdDrawPixel(x0, y0-  r, color);
    ILI9225_lcdDrawPixel(x0 + r, y0, color);
    ILI9225_lcdDrawPixel(x0 - r, y0, color);

    while (x < y) {
        if (f >= 0) {
            y--;
            ddF_y += 2;
            f += ddF_y;
        }
        x++;
        ddF_x += 2;
        f += ddF_x;

        ILI9225_lcdDrawPixel(x0 + x, y0 + y, color);
        ILI9225_lcdDrawPixel(x0 - x, y0 + y, color);
        ILI9225_lcdDrawPixel(x0 + x, y0 - y, color);
        ILI9225_lcdDrawPixel(x0 - x, y0 - y, color);
        ILI9225_lcdDrawPixel(x0 + y, y0 + x, color);
        ILI9225_lcdDrawPixel(x0 - y, y0 + x, color);
        ILI9225_lcdDrawPixel(x0 + y, y0 - x, color);
        ILI9225_lcdDrawPixel(x0 - y, y0 - x, color);
    }
}

/// Draw solid circle
/// @param    x0 center, point coordinate, x-axis
/// @param    y0 center, point coordinate, y-axis
/// @param    radius radius
/// @param    color 16-bit color
void ILI9225_lcdDrawFillCircle(uint16_t x0, uint16_t y0, uint16_t r, uint16_t color)
{ int x;
  int y;
  int err;
  int old_err;
  int ChangeX;
  
  x=0;
  y=-r;
  err=2-2*r;
  ChangeX=1;
  do{
    if(ChangeX) {
      ILI9225_drawLine(x0-x,y0-y,x0-x,y0+y,color);
      ILI9225_drawLine(x0+x,y0-y,x0+x,y0+y,color);
    } // if
    ChangeX=(old_err=err)<=x;
    if (ChangeX)            err+=++x*2+1;
    if (old_err>y || err>x) err+=++y*2+1;
  } while(y<=0);
    
} 

